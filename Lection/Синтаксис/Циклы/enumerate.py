animals = ['cat', 'dog', 'wolf', 'rate', 'dragon']
for i, animal in enumerate(animals, start=1): # в цикле функция enumerate, первым аргументом принимает список, по которой нажо идти, в качестве второго аргумента указываем ключевое слово старт, и значение с которого хотим начать, по умолчанию = 0
    print(i, animal)
# в переменную первую помещаем число, а во вторую переменную элемент из списка animals
